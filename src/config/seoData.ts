export type Language = keyof typeof seoData;
export type Format = 'default' | 'pdf' | 'jpg' | 'png' | 'heic' | 'docx' | 'xlsx';

export function getSeoData(lang: string, format?: string) {
  const validLang = (lang && ['de', 'fr', 'it'].includes(lang) ? lang : 'de') as Language;
  const validFormat = (format && ['pdf', 'jpg', 'png', 'heic', 'docx', 'xlsx'].includes(format) ? format : 'default') as Format;

  return seoData[validLang][validFormat];
}
